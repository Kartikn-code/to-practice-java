public class vowels{
    public static void main(String[] args){
        char vowel='a';
        switch(Character.tolowercase(vowel)){
            case 'a':
            System.out.println("It is a vowel!!");
            break;
            case 'e':
            System.out.println("it is a vowel!!");
            break;
            case 'i':
            System.out.println("it is a vowel");
            break;
            case 'o':
            System.out.println("it is a vowel");
            break;
            case 'u':
            System.out.println("it is a vowel");
            break;
            default:
            System.out.println("It is not a vowel!!");
        }
    }
}